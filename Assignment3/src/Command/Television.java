package Command;

public class Television {
    public void turnOn() {
        System.out.println("Телевизор теперь включен.");
    }

    public void turnOff() {
        System.out.println("Телевизор теперь выключен.");
    }

    public void volumeUp() {
        System.out.println("Громкость увеличена.");
    }

    public void volumeDown() {
        System.out.println("Громкость понижена.");
    }

    public void nextChannel() {
        System.out.println("Переключение на следующий канал.");
    }

    public void previousChannel() {
        System.out.println("Переключение на предыдущий канал.");
    }
}

